num = int(input('Введите число: '))
for i in range(0, num, 2):          # от 0 до num через 2
    print(i)

###
# Функция range()
# range(stop) - от 0 до stop с шагом 1
# range(start, stop) - от start до stop с шагом 1
# range(start, stop, step) - от start до stop с шагом step
# * stop не включая
