'''
Вещественные числа

float(x) — возвращает число с плавающей
запятой, составленное из числа или строки x

'''

print(0.1 + 0.2) # ожидаем увидеть 0,3
pi = 3.141_592_653_589_793_238_462_643_383_279_502_884_197_169_399_375
print(pi)       # ожидаем увидеть все число

# 0.30000000000000004   - особенность хранения вещественных чисел
# 3.141592653589793     - вещественный тип занимает определенное число памяти, 
# а вся остальная часть отсекается как ненужная